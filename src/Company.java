 class Company {
    private String name;
    private String street;
    private String postcode;
    private String city;
    private int housenumber;

    Company(String name, String street, String postcode, String city, int housenumber){
        this.name = name;
        this.street = street;
        this.postcode = postcode;
        this.city = city;
        this.housenumber = housenumber;
    }

}
